package valueobjects.category;

public class SubscribeCategory {

	private Integer icategoryid;

	private String cname;

	private String ctitle;
	
	private Integer labelNameId;

	public Integer getIcategoryid() {
		return icategoryid;
	}

	public void setIcategoryid(Integer icategoryid) {
		this.icategoryid = icategoryid;
	}

	public String getCname() {
		return cname;
	}

	public void setCname(String cname) {
		this.cname = cname;
	}

	public String getCtitle() {
		return ctitle;
	}

	public void setCtitle(String ctitle) {
		this.ctitle = ctitle;
	}

	public Integer getLabelNameId() {
		return labelNameId;
	}

	public void setLabelNameId(Integer labelNameId) {
		this.labelNameId = labelNameId;
	}

}
